// Erstelle eine Zufallszahl zwischen 5 und 10
let random1 = Math.random () *5 + 6;
console.log(random1);
// Wenn der Wert 10 ist gib aus Ten
if (random1>10 && random1<11) {
    console.log("Ten");
}
// Wenn der Wert 9 ist gib aus Nine
if (random1>9 && random1<10) {
    console.log("Nine");
}
// Wenn der Wert 8 ist gib aus Eight
if (random1>8 && random1<9) {
    console.log("Eight");
}
